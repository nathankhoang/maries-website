/**
 * One-time / re-runnable build tool (run on your machine, NOT shipped to the browser).
 *
 *   node scripts/extract-glossary.mjs
 *
 * Parses translations.html (Marie's curated, expert-validated audiology phrasebook)
 * into lib/glossary.js — the server-side knowledge asset that grounds the AI
 * translator so it uses Marie's approved Vietnamese terminology.
 *
 * Re-run this whenever translations.html is updated, then redeploy.
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import * as cheerio from 'cheerio';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const SRC = join(root, 'translations.html');
const OUT = join(root, 'lib', 'glossary.js');

const clean = (s) =>
  (s || '')
    .replace(/\s+/g, ' ')
    .replace(/ /g, ' ')
    .trim();

const html = await readFile(SRC, 'utf8');
const $ = cheerio.load(html);

/** @type {{category:string,subsection:string,en:string,vi:string,note?:string}[]} */
const records = [];

$('.tabs__panel[data-panel]').each((_, panelEl) => {
  const panel = $(panelEl);
  const category = clean(panel.attr('data-panel'));

  panel.find('.trans-section').each((__, secEl) => {
    const sec = $(secEl);
    const subsection = clean(sec.find('.trans-section__title').first().text());

    // Standard English → Vietnamese phrase tables.
    sec.find('table.trans-table tbody tr').each((___, trEl) => {
      const tds = $(trEl).find('td');
      const en = clean($(tds.get(0)).text());
      const vi = clean($(trEl).find('td.viet').first().text());
      if (en && vi) records.push({ category, subsection, en, vi });
    });

    // Pronoun tables — two shapes:
    //   A: English | Vietnamese | Listen | Usage   (.pronoun-en/.pronoun-vn/.pronoun-note)
    //   B: Pronoun  | Listen | Used when…          (.pronoun-vn/.pronoun-note, no English)
    sec.find('table.pronoun-table tbody tr').each((___, trEl) => {
      const tr = $(trEl);
      const en = clean(tr.find('td.pronoun-en').first().text());
      const vi = clean(tr.find('td.pronoun-vn').first().text());
      const note = clean(tr.find('td.pronoun-note').first().text());
      if (vi) {
        records.push({
          category,
          subsection,
          en: en || '(pronoun)',
          vi,
          ...(note ? { note } : {}),
        });
      }
    });
  });
});

if (records.length === 0) {
  console.error('✗ No records extracted — check translations.html structure.');
  process.exit(1);
}

const byCategory = records.reduce((acc, r) => {
  acc[r.category] = (acc[r.category] || 0) + 1;
  return acc;
}, {});

const banner = `/* AUTO-GENERATED by scripts/extract-glossary.mjs — do not edit by hand.
 * Source: translations.html  ·  ${records.length} curated audiology phrase pairs.
 * Re-run \`npm run extract-glossary\` after editing translations.html, then redeploy.
 */\n`;

await mkdir(dirname(OUT), { recursive: true });
await writeFile(
  OUT,
  `${banner}export default ${JSON.stringify(records, null, 2)};\n`,
  'utf8'
);

console.log(`✓ Wrote ${records.length} records to lib/glossary.js`);
console.log('  By category:', JSON.stringify(byCategory));
